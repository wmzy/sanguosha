// tests/integration/双人对战.test.ts
import { describe, it, expect } from 'vitest';
import { 创建游戏, 开始游戏 } from '@engine/state';
import { 进入下一阶段, 摸牌阶段, 弃牌阶段检查, 弃牌阶段执行 } from '@engine/turn';
import { 使用杀, 使用桃 } from '@engine/effect';
import { 曹操, 刘备 } from '@shared/characters';

describe('双人对战', () => {
  it('应该能完成一局完整的2人游戏流程', () => {
    // 创建游戏
    let 游戏 = 创建游戏([曹操, 刘备]);
    游戏 = 开始游戏(游戏);

    expect(游戏.状态).toBe('进行中');
    expect(游戏.玩家列表.length).toBe(2);
    expect(游戏.当前玩家).toBe('曹操');

    // 第一回合: 准备阶段
    expect(游戏.当前阶段).toBe('准备');
    游戏 = 进入下一阶段(游戏);

    // 判定阶段
    expect(游戏.当前阶段).toBe('判定');
    游戏 = 进入下一阶段(游戏);

    // 摸牌阶段
    expect(游戏.当前阶段).toBe('摸牌');
    const 摸牌结果 = 摸牌阶段(游戏);
    游戏 = 摸牌结果.状态;
    expect(游戏.玩家列表[0].手牌.length).toBe(2);

    // 出牌阶段 - 使用杀
    游戏 = 进入下一阶段(游戏);
    expect(游戏.当前阶段).toBe('出牌');

    // 找到一张杀
    const 杀牌 = 游戏.玩家列表[0].手牌.find(c => c.name === '杀');
    if (杀牌) {
      const 杀结果 = 使用杀(游戏, '曹操', '刘备');
      expect(杀结果.成功).toBe(true);
      游戏 = 杀结果.状态;
      expect(游戏.玩家列表[1].体力).toBe(3);
    }

    // 弃牌阶段
    游戏 = 进入下一阶段(游戏);
    expect(游戏.当前阶段).toBe('弃牌');
    const 需要弃牌 = 弃牌阶段检查(游戏);
    if (需要弃牌) {
      游戏 = 弃牌阶段执行(游戏, [0]); // 弃第一张牌
    }

    // 结束阶段
    游戏 = 进入下一阶段(游戏);
    expect(游戏.当前阶段).toBe('结束');

    // 进入下一回合 (刘备的回合)
    游戏 = 进入下一阶段(游戏);
    expect(游戏.当前阶段).toBe('准备');
    expect(游戏.当前玩家).toBe('刘备');
    expect(游戏.回合数).toBe(2);
  });

  it('应该正确处理伤害和死亡', () => {
    const 游戏 = 创建游戏([曹操, 刘备]);
    游戏.状态 = '进行中';

    // 对刘备造成4点伤害 (致命)
    let 当前游戏 = 游戏;
    for (let i = 0; i < 4; i++) {
      const 结果 = 使用杀(当前游戏, '曹操', '刘备');
      当前游戏 = 结果.状态;
    }

    const 刘备玩家 = 当前游戏.玩家列表.find(p => p.name === '刘备')!;
    expect(刘备玩家.体力).toBe(0);
  });

  it('应该正确使用桃恢复体力', () => {
    const 游戏 = 创建游戏([曹操, 刘备]);
    游戏.状态 = '进行中';
    游戏.当前玩家 = '曹操';

    // 先受伤
    const 伤害结果 = 使用杀(游戏, '曹操', '刘备');
    expect(伤害结果.状态.玩家列表[1].体力).toBe(3);

    // 给刘备一张桃 (注: 使用桃仅检查体力上限，不检查手牌)
    const 受伤游戏 = 伤害结果.状态;
    受伤游戏.玩家列表[1].手牌 = [{ name: '桃', 类型: '基本牌', 子类型: '桃', 花色: '♥', 点数: '7', 描述: '' }];

    // 刘备使用桃
    const 桃结果 = 使用桃(受伤游戏, '刘备');
    expect(桃结果.成功).toBe(true);
    expect(桃结果.状态.玩家列表[1].体力).toBe(4);
  });
});
