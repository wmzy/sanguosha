// tests/unit/效果.test.ts
import { describe, it, expect } from 'vitest';
import {
  使用杀, 使用桃,
  使用过河拆桥, 使用顺手牵羊, 使用无中生有, 使用决斗,
  使用万箭齐发, 使用南蛮入侵, 使用桃园结义, 使用五谷丰登,
} from '@engine/effect';
import { 创建游戏 } from '@engine/state';
import { 曹操, 刘备 } from '@shared/characters';

describe('卡牌效果', () => {
  describe('使用杀', () => {
    it('应该对目标造成1点伤害', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.当前玩家 = '曹操';

      const 结果 = 使用杀(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(true);
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(刘备玩家.体力).toBe(3);
    });

    it('不能对自己使用杀', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';

      const 结果 = 使用杀(游戏, '曹操', '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用桃', () => {
    it('应该恢复1点体力', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.当前玩家 = '曹操';
      游戏.玩家列表[0].体力 = 3;

      const 结果 = 使用桃(游戏, '曹操');
      expect(结果.成功).toBe(true);
      expect(结果.状态.玩家列表[0].体力).toBe(4);
    });

    it('不能超过体力上限', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.当前玩家 = '曹操';
      // 曹操体力已满 (4/4)

      const 结果 = 使用桃(游戏, '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用过河拆桥', () => {
    it('应该弃置目标一张手牌', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.当前玩家 = '曹操';
      游戏.玩家列表[1].手牌 = [
        { name: '杀', 类型: '基本牌', 子类型: '杀', 花色: '♠', 点数: '3', 描述: '' },
      ];

      const 结果 = 使用过河拆桥(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(true);
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(刘备玩家.手牌.length).toBe(0);
      expect(结果.状态.弃牌堆.length).toBe(1);
    });

    it('目标没有手牌时失败', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      // 刘备没有手牌

      const 结果 = 使用过河拆桥(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(false);
    });

    it('不能对自己使用', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      const 结果 = 使用过河拆桥(游戏, '曹操', '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用顺手牵羊', () => {
    it('应该从目标获得一张手牌', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.当前玩家 = '曹操';
      游戏.玩家列表[1].手牌 = [
        { name: '闪', 类型: '基本牌', 子类型: '闪', 花色: '♥', 点数: '3', 描述: '' },
      ];

      const 结果 = 使用顺手牵羊(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(true);
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      expect(刘备玩家.手牌.length).toBe(0);
      expect(曹操玩家.手牌.length).toBe(1);
    });

    it('目标没有牌时失败', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';

      const 结果 = 使用顺手牵羊(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用无中生有', () => {
    it('应该摸2张牌', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      const 牌堆大小 = 游戏.牌堆.length;

      const 结果 = 使用无中生有(游戏, '曹操');
      expect(结果.成功).toBe(true);
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      expect(曹操玩家.手牌.length).toBe(2);
      expect(结果.状态.牌堆.length).toBe(牌堆大小 - 2);
    });

    it('牌堆不足时失败', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.牌堆 = [];

      const 结果 = 使用无中生有(游戏, '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用决斗', () => {
    it('应该对目标造成1点伤害', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';

      const 结果 = 使用决斗(游戏, '曹操', '刘备');
      expect(结果.成功).toBe(true);
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(刘备玩家.体力).toBe(3);
    });

    it('不能对自己使用', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      const 结果 = 使用决斗(游戏, '曹操', '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用万箭齐发', () => {
    it('应该对所有其他存活玩家造成1点伤害', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';

      const 结果 = 使用万箭齐发(游戏, '曹操');
      expect(结果.成功).toBe(true);
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(曹操玩家.体力).toBe(4); // 使用者不受伤害
      expect(刘备玩家.体力).toBe(3); // 目标受伤
    });
  });

  describe('使用南蛮入侵', () => {
    it('应该对所有其他存活玩家造成1点伤害', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';

      const 结果 = 使用南蛮入侵(游戏, '曹操');
      expect(结果.成功).toBe(true);
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(曹操玩家.体力).toBe(4);
      expect(刘备玩家.体力).toBe(3);
    });
  });

  describe('使用桃园结义', () => {
    it('应该恢复所有存活玩家1点体力', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.玩家列表[0].体力 = 3;
      游戏.玩家列表[1].体力 = 2;

      const 结果 = 使用桃园结义(游戏, '曹操');
      expect(结果.成功).toBe(true);
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(曹操玩家.体力).toBe(4);
      expect(刘备玩家.体力).toBe(3);
    });

    it('不能超过体力上限', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      // 两人体力都已满

      const 结果 = 使用桃园结义(游戏, '曹操');
      expect(结果.成功).toBe(false);
    });
  });

  describe('使用五谷丰登', () => {
    it('应该让每名存活玩家摸1张牌', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      const 牌堆大小 = 游戏.牌堆.length;

      const 结果 = 使用五谷丰登(游戏, '曹操');
      expect(结果.成功).toBe(true);
      const 曹操玩家 = 结果.状态.玩家列表.find(p => p.name === '曹操')!;
      const 刘备玩家 = 结果.状态.玩家列表.find(p => p.name === '刘备')!;
      expect(曹操玩家.手牌.length).toBe(1);
      expect(刘备玩家.手牌.length).toBe(1);
      expect(结果.状态.牌堆.length).toBe(牌堆大小 - 2);
    });

    it('牌堆不足时失败', () => {
      const 游戏 = 创建游戏([曹操, 刘备]);
      游戏.状态 = '进行中';
      游戏.牌堆 = [{ name: '杀', 类型: '基本牌', 子类型: '杀', 花色: '♠', 点数: '3', 描述: '' }];

      const 结果 = 使用五谷丰登(游戏, '曹操');
      expect(结果.成功).toBe(false);
    });
  });
});
