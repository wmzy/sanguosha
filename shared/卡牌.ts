// ============================================================
// 创建一副标准牌堆（含装备和锦囊）
// ============================================================

export function 创建标准牌堆(): Card[] {
  const 牌堆: Card[] = [];

  // 基本牌
  // 杀: ♠3-7, ♣3-7, ♥10-11, ♦6-10
  const 杀花色点数: [Suit, Rank][] = [
    ['♠', '3'], ['♠', '4'], ['♠', '5'], ['♠', '6'], ['♠', '7'],
    ['♣', '3'], ['♣', '4'], ['♣', '5'], ['♣', '6'], ['♣', '7'],
    ['♥', '10'], ['♥', 'J'],
    ['♦', '6'], ['♦', '7'], ['♦', '8'], ['♦', '9'], ['♦', '10'],
  ];

  for (const [花色, 点数] of 杀花色点数) {
    牌堆.push({
      name: '杀',
      类型: '基本牌',
      子类型: '杀',
      花色,
      点数,
      描述: '出牌阶段，对攻击范围内的一名角色使用。其须使用一张【闪】，否则受到1点伤害。',
    });
  }

  // 闪: ♥3-6, ♦10-K
  const 闪花色点数: [Suit, Rank][] = [
    ['♥', '3'], ['♥', '4'], ['♥', '5'], ['♥', '6'],
    ['♦', '10'], ['♦', 'J'], ['♦', 'Q'], ['♦', 'K'],
  ];

  for (const [花色, 点数] of 闪花色点数) {
    牌堆.push({
      name: '闪',
      类型: '基本牌',
      子类型: '闪',
      花色,
      点数,
      描述: '当你成为【杀】的目标时，你可以使用一张【闪】来抵消此【杀】。',
    });
  }

  // 桃: ♥7-9, ♣12
  const 桃花色点数: [Suit, Rank][] = [
    ['♥', '7'], ['♥', '8'], ['♥', '9'],
    ['♣', 'Q'],
  ];

  for (const [花色, 点数] of 桃花色点数) {
    牌堆.push({
      name: '桃',
      类型: '基本牌',
      子类型: '桃',
      花色,
      点数,
      描述: '出牌阶段，对自己使用。回复1点体力。',
    });
  }

  // 装备牌 - 每种各1张
  牌堆.push(...所有武器, ...所有防具, ...所有马);

  // 锦囊牌 - 普通锦囊每种各1张，延时锦囊每种各1张，无懈可击1张
  牌堆.push(...所有锦囊);

  return 牌堆;
}

// ============================================================
// 卡牌分类辅助函数
// ============================================================

export function 是武器牌(牌: Card): boolean {
  return 牌.子类型 === '武器';
}

export function 是防具牌(牌: Card): boolean {
  return 牌.子类型 === '防具';
}

export function 是马牌(牌: Card): boolean {
  return 牌.子类型 === '进攻马' || 牌.子类型 === '防御马';
}

export function 是装备牌(牌: Card): boolean {
  return 牌.类型 === '装备牌';
}

export function 是锦囊牌(牌: Card): boolean {
  return 牌.类型 === '锦囊牌';
}

export function 是延时锦囊(牌: Card): boolean {
  return 牌.锦囊子类型 === '延时锦囊';
}

export function 是黑色花色(牌: Card): boolean {
  return 牌.花色 === '♠' || 牌.花色 === '♣';
}

export function 是红色花色(牌: Card): boolean {
  return 牌.花色 === '♥' || 牌.花色 === '♦';
}
